import pytest

from core.services.reminders import _plural_items


@pytest.mark.parametrize(
    "n, expected",
    [
        (1, "1 незакрытый пункт"),
        (2, "2 незакрытых пункта"),
        (3, "3 незакрытых пункта"),
        (4, "4 незакрытых пункта"),
        (5, "5 незакрытых пунктов"),
        (10, "10 незакрытых пунктов"),
        (11, "11 незакрытых пунктов"),
        (21, "21 незакрытый пункт"),
        (22, "22 незакрытых пункта"),
        (25, "25 незакрытых пунктов"),
        (101, "101 незакрытый пункт"),
        (112, "112 незакрытых пунктов"),
    ],
)
def test_plural_items(n, expected):
    assert _plural_items(n) == expected
